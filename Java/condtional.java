public class condtional {

    public static void main(String[] args) {

        int temp = 24;

        if (temp >= 30) {
            System.out.println("It is very warm today");
        } else if (24 < temp && temp <= 30) {
            System.out.println("It is warm but not very warm");
        } else if (temp <= 24) {
            System.out.println("Boy would this be cold for my mom");
        }

    }

}
